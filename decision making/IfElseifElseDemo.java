public class IfElseifElseDemo {
  public static void main(String[] args) {
    int num = -4;
    if (num > 0) {
      System.out.println("You entered a positive number!");
    }
    else if (num == 0) {
      System.out.println("You entered zero!");
    }
    else {
      System.out.println("You entered a negative number!");
    }
  }
}
